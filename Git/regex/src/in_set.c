#include <ft_regex.h>

static inline int	in_set(unsigned char c, uint64_t set[2])
{
	if (c > 127)
		return (0);
	if (c > 63)
		return (!!(((uint64_t)1 << (c - 64)) & set[1]));
	return (!!(((uint64_t)1 << c) & set[0]));
}
